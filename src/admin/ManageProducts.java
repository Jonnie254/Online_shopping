    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (isEmpty()) {
            int id = Integer.parseInt(jTextField2.getText());
            String pname = jTextField3.getText();
            String categories = jComboBox1.getSelectedItem().toString();
            int quantity = Integer.parseInt(jTextField4.getText());
            if (isNumeric(jTextField5.getText())) {
                double price = Double.parseDouble(jTextField5.getText());
                if (!productdao.isIDExist(id)) {
                    if (!productdao.isProCatExist(pname, categories)) {
                        productdao.insertProduct(id, pname, categories, quantity, price);
                        jTable1.setModel(new DefaultTableModel(null, new Object[]{"Product ID", "Product Name", "Category", "Quantity", "Price"}));
                        productdao.getCategoriesValue(jTable1, "");
                        clear();
                    } else {
                        JOptionPane.showMessageDialog(this, "Product and Category already exists", "Warning", 2);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Product id does exist", "Warning", 2);
                }
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

