    private void init() {
        jTextField2.setBackground(notedit);
        jTextField2.setText(String.valueOf(categorydao.getRowMax()));
        categoryTable();
    }

    private void categoryTable() {
        categorydao.getCategoryValues(jTable1, "");
        model = (DefaultTableModel) jTable1.getModel();
        jTable1.setRowHeight(30);
        jTable1.setShowGrid(true);
        jTable1.setGridColor(BLACK);
        jTable1.setBackground(WHITE);
        jTable1.setSelectionBackground(LIGHT_GRAY);
    }

    private void clear() {
        jTextField1.setText("");
        jTextField2.setText(String.valueOf(categorydao.getRowMax()));
        jTextField3.setText("");
        jTextField4.setText("");
        jTable1.clearSelection();
    }

    private boolean check() {
        String newcategory = jTextField3.getText(); // New category name
        String oldcategory = (model.getValueAt(rowIndex, 0).toString()); // Old category name
        if (newcategory.equals(oldcategory)) {
            return false; // Names are the same, no need to check or display an error
        } else {
            // Names are different, but we are allowing the update to proceed
            return false; // Return false to indicate that the update can proceed
        }
    }

    private boolean isEmpty() {
        if (jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Category name is required", "Warning", 2);
            return false;
        }
        if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Description of the item is required", "Warning", 2);
            return false;
        }
        return true;
    }
