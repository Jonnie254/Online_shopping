    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (isEmpty()) {
            String email = jTextField1.getText();
            String password = String.valueOf(jPasswordField1.getPassword());
            if (jRadioButton1.isSelected()) {
                try {
                    Connection conn = dbconnection.getConnection();
                    PreparedStatement ps = conn.prepareStatement("select * from user where uemail = ? and upassword = ?");
                    ps.setString(1, email);
                    ps.setString(2, password);
                    ResultSet rs = ps.executeQuery();
                    if (rs.next()) {
                        UserDashboard userdashboard = new UserDashboard();
                        userdashboard.setVisible(true);
                        userdashboard.pack();
                        UserDashboard.useEmail.setText(email);
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid email or username", "Login failed", 2);
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (jRadioButton2.isSelected()) {
                try {
                    Connection conn = dbconnection.getConnection();
                    PreparedStatement ps = conn.prepareStatement("select * from supplier where semail = ? and spassword = ? ");
                    ps.setString(1, email);
                    ps.setString(2, password);
                    ResultSet rs = ps.executeQuery();
                    if (rs.next()) {
                        SupplierDashboard supplierdashboard = new SupplierDashboard();
                        supplierdashboard.pack();
                        supplierdashboard.setVisible(true);
                        SupplierDashboard.suppEmail.setText(email);
                        this.dispose();

                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid email or username", "Login failed", 2);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (jRadioButton3.isSelected()) {
                try {
                    Connection conn = dbconnection.getConnection();
                    PreparedStatement ps = conn.prepareStatement("select * from admin where email = ? and password = ?");
                    ps.setString(1, email);
                    ps.setString(2, password);
                    ResultSet rs = ps.executeQuery();
                    if (rs.next()) {
                        AdminDashboard admindashboard = new AdminDashboard();
                        admindashboard.setVisible(true);
                        admindashboard.pack();
                        AdminDashboard.adminEmail.setText(email);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid email or username", "Login failed", 2);
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

